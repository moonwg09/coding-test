# 1. 전체 테스트 케이스의 개수(T)를 먼저 입력받습니다.
T = int(input())

# 2. T번만큼 테스트를 반복합니다.
for _ in range(T):
    # 공백을 기준으로 숫자와 문자열을 쪼개서 각각 변수에 넣습니다. (예: "3", "ABC")
    num, word = input().split()
    
    # 문자를 곱하기 위해 num을 정수로 바꿔줍니다.
    num = int(num)

    # 3. 단어의 글자를 하나씩 순회하며 곱해서 바로 출력합니다.
    for char in word:
        # 줄바꿈 없이 이어서 출력
        print(char * num, end="")
        
    # 4. 하나의 테스트 케이스(단어 하나) 출력이 끝났으므로, 다음 출력을 위해 줄바꿈을 해줍니다.
    print()