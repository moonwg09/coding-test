# 처음에 입력받은 숫자를 기억해두어야 나중에 비교할 수 있습니다.
n = int(input())
original_n = n  

count = 0

while True:
    # 1. 10의 자리(a)와 1의 자리(b)를 구합니다.
    a = n // 10
    b = n % 10
    
    # 2. 두 숫자를 더한 값의 1의 자리(c)를 구합니다. 
    # (합이 10이 넘을 수 있으므로 % 10을 해줍니다. 예: 8+6=14 -> 4)
    c = (a + b) % 10
    
    # 3. 새로운 숫자를 만듭니다. (원래 1의 자리 b에 10을 곱하고, 합의 1의 자리 c를 더함)
    n = (b * 10) + c
    
    # 사이클이 한 번 돌았으므로 카운트를 1 올립니다.
    count += 1
    
    # 4. 새로 만들어진 숫자가 맨 처음 입력받은 숫자와 같아지면 반복을 멈춥니다.
    if n == original_n:
        break

print(count)